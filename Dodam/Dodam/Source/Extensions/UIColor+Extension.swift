import UIKit

extension UIColor {
    @nonobjc class var mainBlue: UIColor {
        return UIColor(red: 48 / 255.0, green: 71 / 255.0, blue: 94 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var subGray: UIColor {
        return UIColor(red: 236 / 255.0, green: 236 / 255.0, blue: 236 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var pointOrange100: UIColor {
        return UIColor(red: 255 / 255.0, green: 138 / 255.0, blue: 47 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var pointOrange200: UIColor {
        return UIColor(red: 247 / 255.0, green: 149 / 255.0, blue: 73 / 255.0, alpha: 1.0)
    }
}
